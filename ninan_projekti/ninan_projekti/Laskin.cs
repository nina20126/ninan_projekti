using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ninan_projekti
{
    public class Laskin
    {
        public Laskin()
        {
        }

        public int Summa(int luku1, int luku2)
        {
            return luku1 + luku2;
        }

        public int Erotus(int luku1, int luku2)
        {
            return luku1 - luku2;
        }
    }
}
